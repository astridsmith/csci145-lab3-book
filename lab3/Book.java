public class Book
{
    private String title;
    private int pages;
    public String getTitle()
    {
        return title;
    }
    public int getPages()
    {
        return pages;
    }
    public void setTitle(String t)
    {
        title = t;
    }
    public void setPages(int p)
    {
        pages = p;
    }
}
